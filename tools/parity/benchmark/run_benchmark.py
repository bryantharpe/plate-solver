#!/usr/bin/env python3
"""CLI entrypoint for the eval-harness: benchmarks & parity-compares three
plate-solving implementations (tetra3_original, cedar_flow, ps_grpc) on the
shared image corpus, writing a machine-readable ``results.json``.

Run (from the repo root, using the ``tools/parity/.venv`` interpreter that
has cedar-solve + grpcio installed):

    tools/parity/.venv/bin/python tools/parity/benchmark/run_benchmark.py

Expects ``corpus.py`` (in this same directory) to define:
    ASTRONOMICAL_IMAGES: list[str]        # 9 filenames, a valid solve expected
    STRESS_IMAGES: list[str]              # 2 filenames, no valid solve expected
    CORPUS: list[CorpusImage]              # all 11, each with .name/.path/.category

and the following to already have been built/generated by the environment &
fixtures pass (openspec/changes/feat-09-eval-harness/tasks.md, group 1):
    target/release/ps-grpc
    reference-solutions/cedar-detect/target/release/cedar-detect-server
    tools/parity/benchmark/generated/shared_catalog.bin
    tools/parity/benchmark/generated/plate_solver_pb2*.py
    tools/parity/.venv-tetra3-orig/
"""
from __future__ import annotations

import argparse
import json
import os
import platform
import socket
import sys
import traceback
from pathlib import Path
from typing import Any, Dict, List, Optional

BENCHMARK_DIR = Path(__file__).resolve().parent
ROOT = BENCHMARK_DIR.parents[2]
GENERATED_DIR = BENCHMARK_DIR / "generated"

SHARED_CATALOG_BIN = GENERATED_DIR / "shared_catalog.bin"
CEDAR_SOLVE_DB = ROOT / "reference-solutions" / "cedar-solve" / "tetra3" / "data" / "default_database.npz"
TETRA3_ORIGINAL_DB = ROOT / "reference-solutions" / "tetra3" / "tetra3" / "data" / "default_database.npz"
TETRA3_ORIGINAL_VENV_PYTHON = ROOT / "tools" / "parity" / ".venv-tetra3-orig" / "bin" / "python"

sys.path.insert(0, str(BENCHMARK_DIR))

import servers  # noqa: E402
from adapters import SHARED_PARAMS, CedarFlowAdapter, PsGrpcAdapter, TetraOriginalAdapter  # noqa: E402

KNOWN_LIMITATIONS = [
    "ps_grpc's Solution.t_extract_ms is hard-coded 0.0 in ps-grpc/src/service.rs for both "
    "SolveFromCentroids and SolveFromImage; this harness gets a real self-reported extraction "
    "time via a standalone ExtractCentroids call instead (see design.md).",
    "ps_solve::solve_from_image hard-codes sigma=4.0, noise_estimate=1.0, binning=1 regardless of "
    "request parameters (CODEBASE-REVIEW.md C2) - a measurement caveat, not fixed by this change.",
    "tetra3_original uses its own bundled default_database.npz (different build than cedar-solve's "
    "shared catalog - incompatible hash-table formats); any tetra3_original comparison is "
    "cross-catalog, not strict same-catalog parity.",
    "Measured on this host (see 'host' in this file's metadata), not the PRD's RPi-4B/mobile target.",
]


def _find_free_port() -> int:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


def _check_prerequisites() -> Optional[str]:
    """Returns an error message describing the first missing prerequisite, or
    None if everything the harness needs is present. All of these are built by
    the environment & fixtures pass (tasks.md group 1), not by this file."""
    missing = []
    if not servers.PS_GRPC_BIN.is_file():
        missing.append(f"{servers.PS_GRPC_BIN} (cargo build --release -p ps-grpc)")
    if not servers.CEDAR_DETECT_BIN.is_file():
        missing.append(
            f"{servers.CEDAR_DETECT_BIN} (cargo build --release --manifest-path "
            f"reference-solutions/cedar-detect/Cargo.toml --bin cedar-detect-server)"
        )
    if not SHARED_CATALOG_BIN.is_file():
        missing.append(f"{SHARED_CATALOG_BIN} (ps-db/examples/npz_to_native.rs)")
    if not (GENERATED_DIR / "plate_solver_pb2.py").is_file():
        missing.append(f"{GENERATED_DIR / 'plate_solver_pb2.py'} (compile_plate_solver_proto.py)")
    if not (GENERATED_DIR / "plate_solver_pb2_grpc.py").is_file():
        missing.append(f"{GENERATED_DIR / 'plate_solver_pb2_grpc.py'} (compile_plate_solver_proto.py)")
    if not CEDAR_SOLVE_DB.is_file():
        missing.append(str(CEDAR_SOLVE_DB))
    if not TETRA3_ORIGINAL_DB.is_file():
        missing.append(str(TETRA3_ORIGINAL_DB))
    if not TETRA3_ORIGINAL_VENV_PYTHON.is_file():
        missing.append(f"{TETRA3_ORIGINAL_VENV_PYTHON} (tools/parity/.venv-tetra3-orig)")
    try:
        import corpus

        for attr in ("ASTRONOMICAL_IMAGES", "STRESS_IMAGES", "CORPUS"):
            if not hasattr(corpus, attr):
                missing.append(f"tools/parity/benchmark/corpus.py is missing '{attr}'")
    except ImportError as exc:
        missing.append(f"tools/parity/benchmark/corpus.py ({exc})")

    if not missing:
        return None
    return "Missing prerequisite(s) from the environment & fixtures pass:\n  - " + "\n  - ".join(missing)


def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--output", type=Path, default=BENCHMARK_DIR / "results.json")
    p.add_argument("--iterations-detect", type=int, default=20)
    p.add_argument("--warmup-detect", type=int, default=3)
    p.add_argument("--iterations-solve", type=int, default=5)
    p.add_argument("--warmup-solve", type=int, default=1)
    p.add_argument("--solve-timeout-s", type=float, default=5.0)
    p.add_argument("--stress-timeout-s", type=float, default=5.0)
    p.add_argument("--fov-estimate-deg", type=float, default=None)
    p.add_argument("--fov-max-error-deg", type=float, default=None)
    return p.parse_args()


def _run_one(adapter, image_path: Path, kind: str, args: argparse.Namespace) -> Dict[str, Any]:
    is_stress = kind == "stress"
    n_iter_detect, warmup_detect = (1, 0) if is_stress else (args.iterations_detect, args.warmup_detect)
    n_iter_solve, warmup_solve = (1, 0) if is_stress else (args.iterations_solve, args.warmup_solve)
    timeout_s = args.stress_timeout_s if is_stress else args.solve_timeout_s

    try:
        detect_result = adapter.detect(image_path, n_iter_detect, warmup_detect)
    except Exception as exc:  # noqa: BLE001 - one image's failure must not abort the run
        detect_result = {
            "system": adapter.system,
            "image_name": image_path.name,
            "error": f"{type(exc).__name__}: {exc}",
        }

    try:
        solve_result = adapter.solve_from_image(
            image_path,
            n_iter_solve,
            warmup_solve,
            fov_estimate=args.fov_estimate_deg,
            fov_max_error=args.fov_max_error_deg,
            timeout_s=timeout_s,
        )
    except Exception as exc:  # noqa: BLE001 - one image's failure must not abort the run
        solve_result = {
            "system": adapter.system,
            "image_name": image_path.name,
            "error": f"{type(exc).__name__}: {exc}",
        }

    return {"kind": kind, "detect": detect_result, "solve": solve_result}


def main() -> int:
    args = _parse_args()

    prereq_error = _check_prerequisites()
    if prereq_error:
        print(f"FAIL: {prereq_error}", file=sys.stderr)
        return 1

    import corpus

    ps_grpc_port = _find_free_port()
    cedar_detect_port = _find_free_port()

    print(f"Starting cedar-detect-server on port {cedar_detect_port}...")
    cedar_detect_server = servers.start_cedar_detect_server(cedar_detect_port)
    print(f"Starting ps-grpc on port {ps_grpc_port} (db={SHARED_CATALOG_BIN})...")
    ps_grpc_server = servers.start_ps_grpc_server(ps_grpc_port, SHARED_CATALOG_BIN)

    try:
        ps_grpc_adapter = PsGrpcAdapter(ps_grpc_server.address)
        cedar_flow_adapter = CedarFlowAdapter(cedar_detect_server.address, CEDAR_SOLVE_DB)
        tetra3_original_adapter = TetraOriginalAdapter(TETRA3_ORIGINAL_VENV_PYTHON, TETRA3_ORIGINAL_DB)
        adapters = [ps_grpc_adapter, cedar_flow_adapter, tetra3_original_adapter]

        per_image_results: List[Dict[str, Any]] = []
        for corpus_image in corpus.CORPUS:
            image_name, image_path, kind = corpus_image.name, corpus_image.path, corpus_image.category
            print(f"[{kind}] {image_name}")
            for adapter in adapters:
                print(f"  {adapter.system}...")
                try:
                    per_image_results.append(
                        {"image_name": image_name, **_run_one(adapter, image_path, kind, args)}
                    )
                except Exception:  # noqa: BLE001 - never let one image/system kill the whole run
                    per_image_results.append(
                        {
                            "image_name": image_name,
                            "kind": kind,
                            "detect": {"system": adapter.system, "image_name": image_name, "error": traceback.format_exc()},
                            "solve": {"system": adapter.system, "image_name": image_name, "error": traceback.format_exc()},
                        }
                    )
    finally:
        ps_grpc_server.stop()
        cedar_detect_server.stop()

    output = {
        "metadata": {
            "host": {
                "machine": platform.machine(),
                "system": platform.system(),
                "cpu_count": os.cpu_count(),
            },
            "shared_params": SHARED_PARAMS,
            "iteration_counts": {
                "detect": {"n_iterations": args.iterations_detect, "warmup": args.warmup_detect},
                "solve": {"n_iterations": args.iterations_solve, "warmup": args.warmup_solve},
                "stress": {"n_iterations": 1, "warmup": 0, "timeout_s": args.stress_timeout_s},
            },
            "catalogs": {
                "ps_grpc": PsGrpcAdapter.catalog,
                "cedar_flow": CedarFlowAdapter.catalog,
                "tetra3_original": TetraOriginalAdapter.catalog,
            },
            "known_limitations": KNOWN_LIMITATIONS,
            "corpus": {
                "astronomical": list(corpus.ASTRONOMICAL_IMAGES),
                "stress": list(corpus.STRESS_IMAGES),
            },
        },
        "results": per_image_results,
    }

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(json.dumps(output, indent=2, sort_keys=True) + "\n")
    print(f"Wrote {args.output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
